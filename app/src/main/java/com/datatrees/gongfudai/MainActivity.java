    @SuppressLint("NewApi")
    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);
        if (resultCode != RESULT_OK)
            return;
        if (requestCode == PICK_IMAGE_RC) {
            Uri imageUri1 = data.getData();
            String path = PickUtils.getPath(this, imageUri1);
            new Crop(Uri.fromFile(new File(path))).output(imageUri)
                    .withMazSize(200, 200).start(this);
        } else if (requestCode == TACK_PICTURE_RC) {
            new Crop(imageUri).output(imageUri).withMazSize(200, 200)
                    .start(this);
        } else if (requestCode == Crop.REQUEST_CROP) {
            if (imageUri != null) {
                Bitmap bitmap = BitmapFactory.decodeFile(imageUri.getPath());
                bitmap2file(bitmap);
                Drawable drawable = new BitmapDrawable(null, bitmap);
                image_view.setImageBitmap(null);
                image_view.setBackground(drawable);
            }
        }
    }

    private File bitmap2file(Bitmap bitmap) {
        String stringDate = "image_" + SystemClock.uptimeMillis();
        String folderPath = FileUtils.getExtImageFilesDir().getPath();
        String fileName = stringDate + ".jpg";
        File fileDir = new File(folderPath);
        if (!fileDir.exists()) {
            fileDir.mkdirs();
        }
        File file = new File(folderPath + File.separator + fileName);
        try {
            FileOutputStream out = new FileOutputStream(file);
            if (bitmap.compress(Bitmap.CompressFormat.JPEG, 90, out)) {
                out.flush();
                out.close();
            }
        } catch (FileNotFoundException e) {
            e.printStackTrace();
            return null;
        } catch (IOException e) {
            e.printStackTrace();
            return null;
        }
        return file;
    }

